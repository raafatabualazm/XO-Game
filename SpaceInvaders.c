void checkForWinner(){
	int z, row = xpos, col = ypos - 1 ;
	if(ch == 'x')	z = 1 ;
	else	z = 2 ;
	//check for this column
	sum = 0 ;
	for(i = 0 ; i < 3 ; ++i){
		if(state[row][i] == z)++sum ;
	}
	if(sum == 3)	//there's a winner	
		restart();
	//check for this row
	sum = 0 ;
	for(i = 0 ; i < 3 ; ++i){
		if(state[i][col] == z)++sum ;
	}
	if(sum == 3)	//there's a winner
		restart();
	if(state[0][0] == z && state[1][1] == z && state[2][2] == z)	//there's a winner
		restart();
	if(state[0][2] == z && state[1][1] == z && state[2][0] == z)	//there's a winner
		restart();
}

void restart(void){
	winner = 1 ;
	if(ch == 'x')	++recx;
	else	++recy;
	Nokia5110_ClearBuffer();
	Nokia5110_DisplayBuffer();
	Nokia5110_OutChar(ch) ;
	Nokia5110_OutString(" have won the game!");
	Delay();
	Nokia5110_ClearBuffer();
	Nokia5110_PrintBMP(0, 47, Background, 0);
	Nokia5110_DisplayBuffer();   // draw buffer
	outRes();
	for ( i =0;i <3;i++)
		for( j =0; j<3;j++)
			state[i][j]=0;
}

void checkForDraw(){
	int z = 0 ;
	for(i = 0 ; i < 3 ; ++i){
		for(j = 0 ; j < 3 ; ++j){
			if(state[i][j] == 0)
				z = 1 ;
		}
	}
	if(z == 0){		//there's a draw
		winner = 0 ;
		Nokia5110_ClearBuffer();
		Nokia5110_DisplayBuffer();
		Nokia5110_OutString("No one have won the game!");
		Delay();
		Nokia5110_ClearBuffer();
		Nokia5110_PrintBMP(0, 47, Background, 0);
		Nokia5110_DisplayBuffer();   // draw buffer
		outRes();
		for ( i =0;i <3;i++)
			for( j =0; j<3;j++)
				state[i][j]=0;
		}
}

